int	sum(int x, int y)
{
	return (x + y);
}

int	sub(int x, int y)
{
	return (x - y);
}

int	div(int x, int y)
{
	return (x / y);
}

int	mul(int x, int y)
{
	return (x * y);
}

int	rem(int x, int y)
{
	return (x % y);
}
